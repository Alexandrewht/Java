const a = 7;
let b = 3;

b += a; // b = b + a (soma)
console.log(b);

b -= 4 // b = b - 4 (subtração)
console.log(b);

b *= 2 // b = b * 2 (multiplicação)
console.log(b);

b /= 2 // b = b / 2 (dívisão)
console.log(b);

b %= 2 // b = b % 2 (módulo, restante da operação, porém java lê isso como float e retorna o resto 0)
console.log(b);